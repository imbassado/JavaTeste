public class Exercicio2 {
    public static void main (String[] args){
        int num = 3;
        int i,resultado;
        for(i=0;i<=10;i++){
            System.out.println("tabuada do " + num + ": ");
            resultado = num * i;
            System.out.println(num + " x " + i + " = " + resultado);
        }
    }
}
